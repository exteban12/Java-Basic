package Operadores;

public class Operadores {

    public static void main(String[] args) {

        int a = 6;
        int b = 6;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);
        System.out.println("_____________________");
        // asignacion
        a -= 1;
        System.out.println(a);
        a += 1;
        System.out.println(a);
        a *= 2;
        System.out.println(a);
        a /= 2;
        System.out.println(a);
        a %= 2;
        System.out.println(a);
        
        
        // operadores de comparacion
        
        System.out.println(a == b);
        
        //distincion
        System.out.println(a != b);
        
        System.out.println(a > b);
        System.out.println(a >= b);
        System.out.println(a < b);
        System.out.println(a <= b);
        
        //logicos
        
        
        
        

    }

}
